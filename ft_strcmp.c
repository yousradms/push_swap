#include "push_swap.h"

int	ft_strcmp(char *s1, char *s2)
{
	size_t	i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while ((s1[i] || s2[i]))
	{
		if (s1[i] !=  s2[i])
			return (1);
		i++;
	}
	return (0);
}
